     */
    public function update(Request $request, $id)
    {
      $tagInfo=Tag::findOrFail($id);
     $tagInfo->tag_name = $request->tag;
     $tagInfo->save();
     Toastr::info('Info', 'Tag Information Update success');
     return back();
